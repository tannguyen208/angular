/**
 * random name
 * @param gender boolean
 * @default gender = female
 */
export const randomName = (gender?: boolean) => {
  let firstName = FEMALE[Math.floor(Math.random() * FEMALE.length)];
  let lastName = LAST_NAME[Math.floor(Math.random() * LAST_NAME.length)];

  if (gender) {
    firstName = MALE[Math.floor(Math.random() * MALE.length)];
  }

  return firstName + " " + lastName;
};
